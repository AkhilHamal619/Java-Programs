//javaOperators

public class RelationalOperators 
{
	public static void main(String[]args)
	{
		boolean a;
		a = 5>10;
		System.out.println("5>10 is:"+a);
		
		a = 5<10;
		System.out.println("5<10 is:"+a);
		
		a = 5>=10;
		System.out.println("5>=10 is:"+a);
		
		a = 5<=10;
		System.out.println("5<=10 is:"+a);
		
		a = 5==10;
		System.out.println("5==10 is:"+a);
		
		
	}
}