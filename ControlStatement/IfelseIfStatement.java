package ControlStatement;

public class IfelseIfStatement 
{
    public static void main(String[]args)
    {

    
        int i = 10;
        if(i>100)
        {
            System.out.println("is greater than 100");
        }
        else if(i>50)
        {
            System.out.println("is greater than 50");
        }
        else
        {
            System.out.println("is less than 50");
        }
    }       
}
